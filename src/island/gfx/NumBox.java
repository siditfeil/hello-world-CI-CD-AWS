package island.gfx;

public class NumBox {
	
	private int ligne;
	private int colonne;


	
	
	


	public NumBox(int ligne, int colonne) {
		super();
		this.ligne = ligne;
		this.colonne = colonne;
	}
	
	public int getLigne() {
		return ligne;
	}


	public int getColonne() {
		return colonne;
	}
	
	public void setLigne(int ligne) {
		this.ligne = ligne;
	}

	public void setColonne(int colonne) {
		this.colonne = colonne;
	}
	
	
	

}
